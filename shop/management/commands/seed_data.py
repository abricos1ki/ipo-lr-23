class Command(BaseCommand):
    help = (
        "Заполняет базу данных тестовыми данными: 5 производителей, "
        "10 категорий, 34 товара (правильно привязаны к категориям), "
        "5 покупателей и 1 администратор (с корзинами и профилями)."
    )

    @transaction.atomic
    def handle(self, *args, **options):
        self.stdout.write("Очистка существующих данных...")
        CartItem.objects.all().delete()
        Cart.objects.all().delete()
        Product.objects.all().delete()
        Category.objects.all().delete()
        Manufacturer.objects.all().delete()
        User.objects.filter(is_superuser=False).delete()

        self.stdout.write("Создание производителей...")
        manufacturers = [
            Manufacturer.objects.create(name=name, country=country, description=desc)
            for name, country, desc in MANUFACTURERS
        ]
        man_by_name = {m.name: m for m in manufacturers}

        self.stdout.write("Создание категорий...")
        cat_objects = {
            name: Category.objects.create(name=name, description=CATEGORY_DESCRIPTIONS[name])
            for name in CATEGORIES
        }

        self.stdout.write("Создание товаров...")
        products = []
        for cat_name, name_tpl, desc_tpl, sizes in PRODUCTS:
            category = cat_objects[cat_name]
            manufacturer = random.choice(manufacturers)
            size_val = random.choice(sizes) if sizes else None

            if size_val:
                name = name_tpl.format(m=manufacturer.name, n=size_val)
                desc = desc_tpl.format(m=manufacturer.name, n=size_val)
            else:
                name = name_tpl.format(m=manufacturer.name)
                desc = desc_tpl.format(m=manufacturer.name)

            price = Decimal(random.randrange(500, 30000)) / 100
            price = price.quantize(Decimal("0.01"))
            stock = random.randint(0, 50)

            product = Product.objects.create(
                name=name,
                description=desc,
                price=price,
                stock_quantity=stock,
                category=category,
                manufacturer=manufacturer,
            )
            products.append(product)

        self.stdout.write("Создание пользователей, профилей и корзин...")
        profile_data = {
            "anna":   ("Анна Соколова",    "+375 29 111-22-33", "г. Минск, ул. Сурганова, д. 5, кв. 12",          Profile.EXPERIENCE_PRACTITIONER),
            "ivan":   ("Иван Петров",      "+375 29 222-33-44", "г. Минск, пр-т Независимости, д. 80, кв. 3",     Profile.EXPERIENCE_BEGINNER),
            "olga":   ("Ольга Кузнецова",  "+375 29 333-44-55", "г. Минск, ул. Веры Хоружей, д. 8, кв. 45",      Profile.EXPERIENCE_INSTRUCTOR),
            "dmitry": ("Дмитрий Волков",   "+375 29 444-55-66", "г. Минск, ул. Якубова, д. 64, кв. 9",            Profile.EXPERIENCE_BEGINNER),
            "maria":  ("Мария Новак",      "+375 29 555-66-77", "г. Минск, ул. Притыцкого, д. 29, кв. 17",        Profile.EXPERIENCE_PRACTITIONER),
        }
        usernames = list(profile_data.keys())
        categories_list = list(cat_objects.values())

        for username in usernames:
            user, created = User.objects.get_or_create(
                username=username,
                defaults={"email": f"{username}@example.com"},
            )
            if created:
                user.set_password("password123")
                user.save()

            full_name, phone, address, experience = profile_data[username]
            profile, _ = Profile.objects.get_or_create(user=user)
            profile.full_name = full_name
            profile.phone = phone
            profile.address = address
            profile.experience_level = experience
            profile.favorite_category = random.choice(categories_list)
            profile.save()

            cart, _ = Cart.objects.get_or_create(user=user)
            available = [p for p in products if p.stock_quantity > 0]
            chosen = random.sample(available, k=min(2, len(available)))
            for product in chosen:
                CartItem.objects.create(
                    cart=cart,
                    product=product,
                    quantity=random.randint(1, min(3, product.stock_quantity)),
                )

        self.stdout.write("Создание тестового администратора...")
        manager, created = User.objects.get_or_create(
            username="manager",
            defaults={"email": "manager@example.com", "is_staff": True},
        )
        if created:
            manager.set_password("password123")
        manager.is_staff = True
        manager.save()
        mgr_profile, _ = Profile.objects.get_or_create(user=manager)
        mgr_profile.full_name = "Светлана Менеджерова"
        mgr_profile.phone = "+375 29 999-00-11"
        mgr_profile.address = "г. Минск, ул. Сухаревская, д. 1 (офис)"
        mgr_profile.experience_level = Profile.EXPERIENCE_INSTRUCTOR
        mgr_profile.save()
        Cart.objects.get_or_create(user=manager)

        self.stdout.write(self.style.SUCCESS(
            f"Готово: {len(manufacturers)} производителей, "
            f"{len(cat_objects)} категорий, "
            f"{len(products)} товаров, "
            f"{len(usernames)} покупателей, 1 администратор (manager)."
        ))
        self.stdout.write("Пароль для всех тестовых пользователей: password123")
        self.stdout.write("Администратор (is_staff=True): manager / password123")
